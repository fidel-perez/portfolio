#!/usr/bin/env python3
"""Regenerate microsites/index.html + microsites/_index.json.

Walks `microsites/<slug>/index.html`, extracts the <title> and the
first `<p class="tagline">` or `<p class="subtitle">` it finds, and
writes:

    microsites/_index.json — [{ slug, title, subtitle, mtime, url }]
    microsites/index.html  — directory-index landing page (served by
                             GitHub Pages at /microsites/).

Hidden / underscore-prefixed slugs are skipped (e.g. `_template`).
Idempotent. Stdlib only — no third-party deps so the GitHub Action
runs on the stock setup-python image without `pip install`.

Usage:
    python3 scripts/build_microsites_index.py
"""

from __future__ import annotations

import datetime as _dt
import json
import re
from html import escape
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
MICROSITES_DIR = REPO_ROOT / "microsites"
PUBLIC_BASE = "https://fidel-perez.github.io/portfolio/microsites"


def _extract(html: str, pattern: str) -> str | None:
    m = re.search(pattern, html, re.IGNORECASE | re.DOTALL)
    if not m:
        return None
    return re.sub(r"\s+", " ", m.group(1)).strip()


def _scan() -> list[dict]:
    if not MICROSITES_DIR.exists():
        return []
    entries: list[dict] = []
    for child in sorted(MICROSITES_DIR.iterdir()):
        if not child.is_dir() or child.name.startswith(("_", ".")):
            continue
        index = child / "index.html"
        if not index.exists():
            continue
        html = index.read_text(errors="replace")
        title = _extract(html, r"<title>([^<]+)</title>") or child.name
        subtitle = (
            _extract(html, r'<p class="tagline">([^<]+)</p>')
            or _extract(html, r'<p class="subtitle">([^<]+)</p>')
            or ""
        )
        entries.append(
            {
                "slug": child.name,
                "title": title,
                "subtitle": subtitle[:200],
                "mtime": _dt.datetime.fromtimestamp(index.stat().st_mtime).isoformat(
                    timespec="seconds"
                ),
                "url": f"{PUBLIC_BASE}/{child.name}/",
            }
        )
    return entries


