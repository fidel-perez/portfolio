def _render_html(entries: list[dict]) -> str:
    if entries:
        items = "\n".join(
            (
                "  <li>\n"
                f'    <a class="title" href="./{escape(e["slug"])}/" rel="noopener">{escape(e["title"])}</a>\n'
                f'    <p class="sub">{escape(e["subtitle"])}</p>\n'
                f'    <span class="stamp">{escape(e["slug"])} · build {escape(e["mtime"])}</span>\n'
                "  </li>"
            )
            for e in entries
        )
        body = f'<ul class="entries">\n{items}\n</ul>'
    else:
        body = '<p class="lead"><em>No microsites yet.</em></p>'
    return HTML_TEMPLATE.format(
        body=body,
        count=len(entries),
        generated_at=_dt.date.today().isoformat(),
    )


def regenerate() -> int:
    entries = _scan()
    MICROSITES_DIR.mkdir(parents=True, exist_ok=True)
    json_path = MICROSITES_DIR / "_index.json"
    html_path = MICROSITES_DIR / "index.html"
    json_path.write_text(json.dumps(entries, indent=2) + "\n")
    html_path.write_text(_render_html(entries))
    print(f"[ok] {len(entries)} entries -> {json_path.name}, {html_path.name}")
    return 0


if __name__ == "__main__":
    raise SystemExit(regenerate())
